<section>
  <h3 class="mb-2 font-bold">Actions</h3>
  <p>The toggles control the public visibility of the feature:</p>
  <ul class="ml-6 mt-2 list-disc space-y-2">
    <li>
      <span class="font-semibold">ACCESSIBLE & OPEN TO PUBLIC</span> - Feature is shown on
      the map if its layer is active. Set true if, in ordinary circumstances, the feature
      is accessible to the public without restrictions or permission. If the feature is temporarily
      inaccessible, this should be false.
    </li>
    <li>
      <span class="font-semibold">INTANGIBLE FEATURE</span> - Feature may be shown on the
      map with the caveat that there is (no longer) a physical presence on site. This invites
      visitors to experience the feature's practical, historic context - e.g. sites of historic
      events; a striking contrast with the present; or a locality that is more salient than
      the missing materiality of the feature.
    </li>
  </ul>
  <p class="mt-2 text-sm italic">
    Note that is a feature is permanently unavailable AND there is no historic,
    intangible value to referencing the site, then it should simply to
    <u>unpublished</u> to delist it from all maps.
  </p>
</section>

